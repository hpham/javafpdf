package net.sourceforge.javafpdf;

/**
 * PDF display layout.
 *
 * @author Alan Plum
 * @version $Rev: 8 $
 * @since 4 Mar 2008
 */
public enum Layout {
    /**
     * Single page layout.
     */
    SINGLE,
    /**
     * Continuous layout.
     */
    CONTINUOUS,
    /**
     * Two pages layout.
     */
    TWO,
    /**
     * Default layout.
     */
    DEFAULT;
}