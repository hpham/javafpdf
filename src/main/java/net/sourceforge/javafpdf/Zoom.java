package net.sourceforge.javafpdf;

/**
 * PDF zoom mode.
 *
 * @author Alan Plum
 * @version $Rev: 8 $
 * @since 4 Mar 2008
 */
public enum Zoom {
    /**
     * Full page zoom.
     */
    FULLPAGE,
    /**
     * Full width zoom.
     */
    FULLWIDTH,
    /**
     * Real zoom.
     */
    REAL,
    /**
     * Default zoom.
     */
    DEFAULT;
}